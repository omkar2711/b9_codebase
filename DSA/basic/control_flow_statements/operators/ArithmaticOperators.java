package DSA.basic.control_flow_statements.operators;

public class ArithmaticOperators {
    public static void main(String[] args)
    {
        int a = 20;
        int b = 50;
        System.out.println("Addition of "+a+" and "+b+" is "+(a+b));//Addition
        System.out.println("Substraction of "+a+" and "+b+" is "+(a-b));//Substraction
        System.out.println("Multiplication of "+a+" and "+b+" is "+(a*b));//Multiplication
        float div = a/b;
        System.out.println("Division of "+a+" and "+b+" is "+div);//Division
        System.out.println("Modulas of "+a+" and "+b+" is "+(a%b));
    }
}
