package DSA.Arrays.operators;

public class IncrementDecrementOperator {
    public static void main(String[] args) {
        int a = 5;
        //Increment  ++
        //Decrement  --
        System.out.println("a = "+a);
        a++;
        System.out.println("a after ++ :"+a); //+1
        a--;
        System.out.println("a after -- :"+a); //-1
    }
}
