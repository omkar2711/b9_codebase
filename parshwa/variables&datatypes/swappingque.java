

public class swappingque {
    public static void main(String[] args) {
        int a=10;
        int b=20;
        
        System.out.println("Value of A=" + a + " Value of B=" + b);

        int temp=a;
        a=b;
        b=temp;
        System.out.println("After Swapping value of A="+a+"value of B="+b);

    }
}
