public class datatypes {
    public static void main(String[] args) {

        // variables: it is used to assign a values.it store a data or it allocate memory
        //premitive data typs
        //int it  store integer value  
        int a=10;
        System.out.println("Vslue of a="+a);

        //char it store character value
        char b = 'A';
        System.out.println("Value of b=" + b);
        
        //float it it use for storing decimal value
        float c = 10.30f;
        System.out.println("Value of c ="+c);

        // double it is to store decimal value
        double d = 18.56;
        System.out.println("Value of  d = "+d);

        // boolen use to to return true of false values
        int f=10;
        int g=20;
        System.out.println(f > g); // it return false because f is less than g

    }
}
